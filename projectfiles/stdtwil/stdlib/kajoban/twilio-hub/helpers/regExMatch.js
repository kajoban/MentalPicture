module.exports = (body) => {
    //matches regex with leading and trailing whitespaces 
    //look at that beast 
    //^(\s*)([0-5])(\s*)+(,)(\s*)(2[0-4]|[01]?[0-9])(\s*)+(,)(\s*)([0-9]|[1-8][0-9]|9[0-9]|[1-8][0-9]{2}|9[0-8][0-9]|99[0-9]|[1-4][0-9]{3}|5000)(\s*)+(,)(\s*)(2[0-4]|[01]?[0-9])(\s*)$

    let re = /^(\s*)([0-5])(\s*)+(,)(\s*)(2[0-4]|[01]?[0-9])(\s*)+(,)(\s*)([0-9]|[1-8][0-9]|9[0-9]|[1-8][0-9]{2}|9[0-8][0-9]|99[0-9]|[1-4][0-9]{3}|5000)(\s*)+(,)(\s*)(2[0-4]|[01]?[0-9])(\s*)$/g

    if(re.test(body) == true){
        return true; 
    } else {
        return false; 
    }

}